// Initialize gallery on DOM load
document.addEventListener("DOMContentLoaded", function() {
  renderGallery("all");
  setupFilterButtons();
  setupGalleryModal();
});

let activeFilter = "all";
let activeGalleryItems = galleryImages.slice();
let activeModalIndex = 0;

const galleryModal = {
  root: null,
  frame: null,
  title: null,
  description: null,
  prev: null,
  next: null
};

// Render gallery items
function renderGallery(filter) {
  const galleryGrid = document.getElementById("galleryGrid");
  galleryGrid.innerHTML = "";

  const filteredImages = filter === "all"
    ? galleryImages
    : galleryImages.filter(img => img.category === filter);

  activeFilter = filter;
  activeGalleryItems = filteredImages;

  filteredImages.forEach((image, index) => {
    const galleryItem = document.createElement("div");
    galleryItem.className = "gallery-item";
    galleryItem.setAttribute("role", "button");
    galleryItem.setAttribute("tabindex", "0");
    galleryItem.setAttribute("aria-label", `Open ${image.title}`);
    galleryItem.dataset.imageId = String(image.id);
    galleryItem.style.transitionDelay = `${index * 0.04}s`;
    galleryItem.innerHTML = `
      <div class="gallery-item-img-wrapper">
        <img src="${image.image}" alt="${image.title}" loading="lazy">
        <div class="gallery-overlay">
          <div class="gallery-content">
            <h3>${image.title}</h3>
            <p>${image.description}</p>
          </div>
        </div>
      </div>
    `;
    galleryGrid.appendChild(galleryItem);

    // Trigger staggered entry transitions after initial DOM paint.
    requestAnimationFrame(() => {
      galleryItem.classList.add("is-visible");
    });

    galleryItem.addEventListener("click", function() {
      openGalleryModalById(image.id);
    });

    galleryItem.addEventListener("keydown", function(event) {
      if (event.key === "Enter" || event.key === " ") {
        event.preventDefault();
        openGalleryModalById(image.id);
      }
    });
  });
}

function transitionGallery(filter) {
  const galleryGrid = document.getElementById("galleryGrid");
  galleryGrid.classList.add("is-transitioning");

  setTimeout(() => {
    renderGallery(filter);
    galleryGrid.classList.remove("is-transitioning");
  }, 180);
}

// Setup filter button functionality
function setupFilterButtons() {
  const filterButtons = document.querySelectorAll(".filter-btn");

  filterButtons.forEach(button => {
    button.addEventListener("click", function() {
      // Remove active class from all buttons
      filterButtons.forEach(btn => btn.classList.remove("active"));

      // Add active class to clicked button
      this.classList.add("active");

      // Filter gallery
      const filterValue = this.getAttribute("data-filter");
      transitionGallery(filterValue);

      // Scroll to top of gallery
      const galleryGrid = document.getElementById("galleryGrid");
      if (galleryGrid) {
        galleryGrid.scrollIntoView({ behavior: "smooth", block: "start" });
      }
    });
  });
}

function setupGalleryModal() {
  galleryModal.root = document.getElementById("gallery-modal");
  galleryModal.frame = document.getElementById("gallery-modal-frame");
  galleryModal.title = document.getElementById("gallery-modal-title");
  galleryModal.description = document.getElementById("gallery-modal-description");
  galleryModal.prev = document.getElementById("gallery-prev");
  galleryModal.next = document.getElementById("gallery-next");

  if (!galleryModal.root || !galleryModal.frame || !galleryModal.title || !galleryModal.description || !galleryModal.prev || !galleryModal.next) {
    return;
  }

  const closeTriggers = galleryModal.root.querySelectorAll("[data-gallery-modal-close]");
  closeTriggers.forEach(function(element) {
    element.addEventListener("click", closeGalleryModal);
  });

  galleryModal.prev.addEventListener("click", function() {
    navigateGalleryModal(-1);
  });

  galleryModal.next.addEventListener("click", function() {
    navigateGalleryModal(1);
  });

  document.addEventListener("keydown", function(event) {
    if (!galleryModal.root || galleryModal.root.hidden) {
      return;
    }

    if (event.key === "Escape") {
      closeGalleryModal();
      return;
    }

    if (event.key === "ArrowLeft") {
      navigateGalleryModal(-1);
      return;
    }

    if (event.key === "ArrowRight") {
      navigateGalleryModal(1);
    }
  });
}

function openGalleryModalById(imageId) {
  if (!galleryModal.root || !activeGalleryItems.length) {
    return;
  }

  const itemIndex = activeGalleryItems.findIndex(function(item) {
    return item.id === imageId;
  });

  if (itemIndex === -1) {
    return;
  }

  activeModalIndex = itemIndex;
  syncModalContent();
  galleryModal.root.hidden = false;
  galleryModal.root.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
}

function closeGalleryModal() {
  if (!galleryModal.root || galleryModal.root.hidden) {
    return;
  }

  galleryModal.root.hidden = true;
  galleryModal.root.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
  galleryModal.frame.removeAttribute("src");
  galleryModal.frame.removeAttribute("srcdoc");
}

function navigateGalleryModal(direction) {
  if (!activeGalleryItems.length) {
    return;
  }

  const listLength = activeGalleryItems.length;
  activeModalIndex = (activeModalIndex + direction + listLength) % listLength;
  syncModalContent();
}

function syncModalContent() {
  const currentItem = activeGalleryItems[activeModalIndex];
  if (!currentItem) {
    return;
  }

  galleryModal.title.textContent = currentItem.title;
  galleryModal.description.textContent = currentItem.description;
  const safeSrc = encodeURI(currentItem.image);
  const safeAlt = currentItem.title.replace(/"/g, "&quot;");
  galleryModal.frame.srcdoc = `<!doctype html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<style>
  html, body {
    margin: 0;
    width: 100%;
    height: 100%;
    overflow: hidden;
    background: #0b1f46;
  }
  img {
    width: 100%;
    height: 100%;
    object-fit: cover;
    object-position: center;
    display: block;
  }
</style>
</head>
<body>
  <img src="${safeSrc}" alt="${safeAlt}">
</body>
</html>`;
}
